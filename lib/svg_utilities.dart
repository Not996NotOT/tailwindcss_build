/// SVG构建器帮助类
class SvgBuilder {
  String _assetPath = '';
  double? _width;
  double? _height;
  Color? _color;
  BoxFit _fit = BoxFit.contain;
  
  /// 设置资源路径
  SvgBuilder asset(String path) {
    _assetPath = path;
    return this;
  }
  
  /// 设置尺寸
  SvgBuilder size(double width, double height) {
    _width = width;
    _height = height;
    return this;
  }
  
  /// 设置宽度
  SvgBuilder width(double width) {
    _width = width;
    return this;
  }
  
  /// 设置高度
  SvgBuilder height(double height) {
    _height = height;
    return this;
  }
  
  /// 设置颜色
  SvgBuilder color(Color color) {
    _color = color;
    return this;
  }
  
  /// 设置适应方式
  SvgBuilder fit(BoxFit fit) {
    _fit = fit;
    return this;
  }
  
  /// 构建SVG组件
  Widget build() {
    return ColorFiltered(
      colorFilter: _color != null 
        ? ColorFilter.mode(_color!, BlendMode.srcIn)
        : const ColorFilter.mode(Colors.transparent, BlendMode.multiply),
      child: Image.asset(
        _assetPath,
        width: _width,
        height: _height,
        fit: _fit,
      ),
    );
  }
}
